#!/bin/bash
#===============================================================================
# Proxmox リモートSSHセットアップ ラッパースクリプト
#
# ローカルマシンからProxmoxにSSH接続してVM内のSSHサーバーを設定します
#===============================================================================

#-------------------------------------------------------------------------------
# 設定変数
#-------------------------------------------------------------------------------
PROXMOX_HOST="192.168.0.147"
PROXMOX_USER="root"
PROXMOX_PORT="22"

SCRIPT_NAME="setup-ssh-via-qm.sh"
SCRIPT_PATH="$(dirname "$0")/${SCRIPT_NAME}"

#-------------------------------------------------------------------------------
# カラー出力
#-------------------------------------------------------------------------------
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_error() { echo -e "${RED}[ERROR]${NC} $1"; }

#-------------------------------------------------------------------------------
# メイン処理
#-------------------------------------------------------------------------------
main() {
    echo ""
    echo "==============================================================================="
    echo "  Proxmox リモートSSHセットアップ"
    echo "==============================================================================="
    echo ""

    # スクリプト存在確認
    if [[ ! -f "$SCRIPT_PATH" ]]; then
        log_error "セットアップスクリプトが見つかりません: $SCRIPT_PATH"
        exit 1
    fi

    log_info "Proxmox ($PROXMOX_HOST) に接続中..."

    # スクリプトをProxmoxにコピー
    log_info "セットアップスクリプトを転送中..."
    scp -P $PROXMOX_PORT "$SCRIPT_PATH" ${PROXMOX_USER}@${PROXMOX_HOST}:/tmp/${SCRIPT_NAME}

    if [[ $? -ne 0 ]]; then
        log_error "スクリプト転送に失敗しました"
        exit 1
    fi

    log_success "スクリプト転送完了"

    # リモートで実行
    log_info "リモートでSSHセットアップを実行中..."
    ssh -p $PROXMOX_PORT ${PROXMOX_USER}@${PROXMOX_HOST} "chmod +x /tmp/${SCRIPT_NAME} && /tmp/${SCRIPT_NAME} $1"

    if [[ $? -eq 0 ]]; then
        log_success "SSHセットアップが完了しました"
    else
        log_error "SSHセットアップ中にエラーが発生しました"
        exit 1
    fi
}

main "$@"
